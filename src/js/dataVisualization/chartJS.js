
export function renderChart(selector) {
    const ctx = document.querySelector(`.${selector}`);
    const canvas = document.createElement("canvas");
    ctx.appendChild(canvas);

    new Chart(canvas, {
        type: 'bar',
        data: {
            labels: ['Red', 'Blue', 'Yellow', 'Green', 'Purple', 'Orange'],
            datasets: [{
                label: '# of Votes',
                data: [12, 19, 3, 5, 2, 3],
                borderWidth: 1
            }]
        },
        options: {
            scales: {
                y: {
                    beginAtZero: true
                }
            }
        }
    });
}
